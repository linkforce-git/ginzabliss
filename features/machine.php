            </div>
            <p class="txta">私たちが使用する「エクレアプロ」は、画期的な照射管理機能によりこの熱量問題を解決しています。<br>その機能とは、<em>「予め照射熱量を機械に設定して照射を始めると、設定熱量を照射し終わった時点で照射を自動停止する」という機能</em>です。</p>
