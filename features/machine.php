            </figure>
            <p class="txta">施術者はパス数もスピードも考慮する必要がありません。照射をスタートしたら、タイマーがゼロになるまで照射器をまんべんなく滑らせていればいいだけです。照射熱量を「パス数」ではなく「時間数」で管理するプロトコルなため、施術者によって熱量が減るといったことが理論上起こり得ません。</p>
