            <p class="txta">多くのクリニックが採用している旧式蓄熱脱毛機には致命的な欠陥があります。<br>その欠陥とは、照射熱量が「施術者が照射器を動かすスピード」によって変動し、計画熱量を正しく照射することがまったくできないという点です。<br>どういうことか、詳しく解説していきます。</p>
            <p class="txta">まず、蓄熱式脱毛機の照射器からは、一定間隔でレーザーが照射され続けています。機器にもよりますが、毎秒5～10発くらいです。</p>
