            </div>
            <p class="txta">ブレンドレーザー脱毛機は2種ないし3種すべてのレーザーを同時照射できる脱毛機です。それゆえ<em>苦手な毛がなく、どんな部位でも高い効果を発揮</em>します。<br>ただ弱点もあります。<br>それぞれの波長成分がおおむね同量（同熱量）ずつ照射されており、その配分が変えられない点です。波長成分の配分を変えられないため、例えばVIOを照射しようと思ったときに「浅いところは熱を入れず、その減らした分をヤグレーザーに寄せて深いところに最大限熱を入れる」といったことができません。<br>それゆえ、ブレンドレーザー脱毛機はどんな部位にも高い効果を発揮するものの、<em>剛毛に対してだけはヤグ単体照射が可能な脱毛機には劣る</em>だろうと考えられます。</p>

            <div id="sec4-4" class="h3a y">
