            <p class="txta">実際に計算してみましょう。<br>例えば熱破壊式照射において、肌1㎠に対して「25J」という出力で「1発だけ」レーザーを照射した場合、照射熱量は25J/㎠です。（この数値は、熱破壊式脱毛における肌1㎠への平均的な照射熱量値です。）</p>
            <figure class="box">
                <figcaption class="cap2 y"><b>熱破壊式</b>における<br>照射熱量の計算</figcaption>
                <picture>
                    <source type="image/webp" srcset="/assets/img/knowledge/effect/fig17.png.webp">
                    <img class="fig17" src="/assets/img/knowledge/effect/fig17.png">
