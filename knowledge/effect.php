            <p class="txta">このように照射熱量は、出力（レーザー1発の威力）に照射回数を掛けることによって算出します。<br>ちなみに熱破壊式と蓄熱式を比較すると、出力（レーザー1発の威力）は熱破壊式のほうが高く、照射熱量は蓄熱式のほうが多い、というのが一般的です。</p>

            <div id="sec3-2" class="h3a y">
