            <p class="txta">照射熱量と脱毛効果は比例関係にあり、照射熱量が多いほど脱毛効果は高くなります。逆に少ないと毛は抜けません。<br>というのも…発毛組織を破壊するのは「レーザーによって生じる熱」なのですから、その熱が多ければ、つまり加熱すればするほどよく抜けるというのは当たり前の話です。</p>

            <div id="sec3-3" class="h3a y">
                <div class="shine"></div>
                <h3 class="ttl">熱破壊式より蓄熱式のほうが<br>照射熱量は多いが、蓄熱式の<br>ほうが抜けるわけではない</h3>
            </div>
            <p class="txta">熱破壊式脱毛と蓄熱式脱毛の照射熱量を比較すると、一般的には蓄熱式脱毛のほうが照射熱量が多くなることがほとんどです。<br>しかし熱量が多いからといって、「蓄熱式脱毛のほうが熱破壊式脱毛よりも抜ける」というわけではありません。<br>蓄熱式脱毛は、「細切れに連続してレーザーを照射する」という方式上、レーザーとレーザーの合間でどうしても熱が逃げてしまいます（温度が下がってしまう）。それゆえ、熱破壊式と同等の効果を得るにはおのずと熱破壊式よりも多くの熱量が必要になるわけです。</p>

            <div id="sec3-4" class="h3a y">
                <div class="shine"></div>
                <h3 class="ttl">熱破壊式は出力を下げると<br>照射熱量が減る</h3>
            </div>
            <p class="txta"><em>熱破壊式脱毛は肌の同一部位にレーザーを1発しか照射しません。それゆえ、痛みで出力を下げたときに照射熱量も減ってしまいます。</em><br>しかし蓄熱式は、たとえレーザー1発の出力を落としたとしても照射熱量を減らさずに施術することが可能です。レーザー1発の出力を落とした分だけ照射回数を増やせばいいからです。照射熱量をしっかり担保するという観点では、熱破壊式よりも蓄熱式のほうが優れていると言えるでしょう。</p>

            <div id="sec3-5" class="h3a y">
                <div class="shine"></div>
                <h3 class="ttl">脱毛効果を上げるための<br>ポイント</h3>
            </div>
            <p class="txta">もし熱破壊式で脱毛を受けるなら、痛くても出力を下げてもらわず我慢することがポイントです。<br>もし<em>痛みが不安という場合は蓄熱式のソプラノチタニウムかエクレアプロがお勧め</em>です。この2つの脱毛機は、出力（フルーエンス）を下げた際に自動で照射時間を延長する機能を備えており、照射熱量の減少が一切おきません。</p>

