            </figure>
            <p class="txta">蓄熱式でも同様に計算してみましょう。<br>肌1㎠に対して「8J」という出力で「6発」レーザーを照射した場合、照射熱量は48J/㎠になります。（この熱量数値も、蓄熱式脱毛における一般的な照射熱量の値です。）</p>
            <figure class="box">
                <figcaption class="cap2 y"><b>蓄熱式</b>における<br>照射熱量の計算</figcaption>
