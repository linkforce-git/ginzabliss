                <h3 class="ttl">硬毛化・増毛化って<br>どんな症状？</h3>
            </div>
            <p class="txta">硬毛化とは、一部の毛が施術前よりも太く硬くなる現象です。増毛化とは、施術前より毛の本数が増えてしまう現象です。どちらも顔・肩・二の腕・うなじ・背中など、毛が細く薄いところに起こりやすい症状です。初回の照射を始めてから4~6か月後に症状に気づくことが多く、これは照射後に抜けた毛が次の成長期（部位によるが4～6か月後）を迎えた際に太く生える現象であったり、生えていなかった毛穴から毛が生える現象であるためです。</p>

            <div id="sec4-2" class="h3a y">
                <div class="shine"></div>
                <h3 class="ttl">硬毛化・増毛化の原因</h3>
            </div>
            <p class="txta"><b>原因：照射熱量が足りない</b><br>硬毛化・増毛化のメカニズムは解明されていませんが、照射熱量が足らずに発毛組織を破壊しきれなかった際に、生き残った発毛組織が活性化されてしまうことによって起きると考えられています。</p>

            <div id="sec4-3" class="h3a y">
                <div class="shine"></div>
                <h3 class="ttl">一般的な対策</h3>
            </div>
            <p class="txta"><b>対策方法①：照射熱量を増やす</b><br>硬毛化・増毛化は、照射熱量が足りずに発毛組織を破壊しきれなかった際に、生き残った発毛組織が活性化されてしまうことによって起きるとされています。すなわち、熱量を増やせば発毛組織を破壊することができ、硬毛化・増毛化は理論上治ります。</p>
            <p class="txta"><b>対策方法②：しばらく間隔をあける</b><br>毛は一定の期間で生えかわります。硬毛化・増毛化した毛も同様、一定期間で抜け落ちます。次に生えてくる毛は元の太さ・量に戻っている可能性が高いです。</p>
            <p class="txta"><b>対策方法③：ニードル脱毛に切り替える</b><br>ニードル脱毛とは、毛穴に細い針を挿入し電気を流して発毛組織を破壊する脱毛方法です（レーザーのように熱を使用しません）。針で刺される痛みを伴いますが、部分的に残ってしまった毛を抜くためには有効です。（当院ではおこなっていません。）</p>
