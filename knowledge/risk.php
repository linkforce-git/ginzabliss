            <p class="txta"><b>原因②：肌が乾燥していた</b><br>肌が乾燥しているとお肌のバリア機能が低下し、外部からの刺激に弱くなります。また、水分が少ないと熱が広がりやすいため火傷のリスクが高まります。</p>
            <p class="txta"><b>原因③：自己処理が甘かった</b><br>毛穴から毛が出ていると、その部分のメラニンにレーザーが反応してしまい表皮の火傷に繋がる恐れがあります。脱毛施術の前にしっかり表面に見えている毛を剃ることが大切です。</p>
            <p class="txta"><b>原因④：脱毛方式の問題</b><br>熱で永久脱毛を実現させる仕組みである以上、熱破壊式・蓄熱式どちらにも火傷のリスクはあります。しかし、強いレーザーを一発照射する熱破壊式脱毛の方が、弱い出力のレーザーを連続照射する蓄熱式脱毛よりも火傷リスクは高いです。</p>
            <p class="txta"><b>原因⑤：施術者の技量が低かった</b><br>施術者の技量が低く火傷をしてしまうこともあります。肌の状態をチェックし、その人にあった出力で照射しなければいけませんが、見誤り高出力で照射してしまうと火傷の恐れがあります。脱毛器の扱いに不慣れだったり、操作方法に過ちがある場合も同様です。<br>また、黒子やタトゥーを見逃してしまいその上から照射してしまうと火傷に繋がります。</p>

