                <img class="fig7" src="/assets/img/knowledge/type/fig7.png">
            </figure>
            <p class="txta">医療脱毛において最も大きく効果に影響する因子は「照射熱量」です。<br>照射熱量が多ければ抜けるし、少なければ抜けません。照射熱量と脱毛効果は強い比例関係にあり、高い脱毛効果を得るためには十分な熱量の確保が不可欠です。<br>この「十分な熱量の確保」という観点において、熱破壊式は大きな弱点を抱えています。</p>

