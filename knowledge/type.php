            </div>
            <p class="txta">熱破壊式脱毛は、高出力のレーザーを「1発だけ当てる」という照射方式です。原則として、同一部位に2発以上照射することはないため、レーザー1発の出力を下げると照射熱量が減り、脱毛効果は確実に減少します。<br>痛みに弱い女性にとって、痛みを抑える第一手段である出力ダウン時の効果減は致命的な弱点です。</p>
