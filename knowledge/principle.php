            </div>
            <p class="txta">前述のとおり、毛包幹細胞は毛母細胞へと分化して毛と毛包を構成する細胞です。この毛包幹細胞を死滅させることにより、将来毛となる細胞そのものがなくなって再発毛が阻止されます。<br>なお毛包幹細胞は自身の分裂によってでしか増殖することができません。それゆえ脱毛施術によってすべての細胞が死滅すると、その後再増殖できなくなり、永久に毛が生えてこなくなります。<br>仮にすべての毛包幹細胞が死滅しなかったとしても、その大部分が失われることで毛の再生スピードは極端に遅くなり、目立つレベルに毛が育つ前に抜け落ちるようになります。</p>
